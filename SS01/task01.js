// Sử dụng từ khóa let để khai báo biến i chạy trong vòng lặp for,
// cho i tăng từ 0 đến 5 và in i trong vòng lặp.
//  Thử in biến i ngoài vòng lặp để kiểm tra và kết luận về phạm vi hoạt động của biến được khai báo bằng từ khóa let.

for (let i = 0; i < 6; i++) {
  console.log(i);
}
console.log(i);
